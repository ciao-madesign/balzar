_FALLBACK = ["#####"] * GLYPH_HEIGHT  # unknown char: solid block, never silent


def _to_rows(art: list[str]) -> tuple[int, ...]:
    return tuple(int(row.replace(".", "0").replace("#", "1"), 2) for row in art)


FONT: dict[str, tuple[int, ...]] = {ch: _to_rows(art) for ch, art in _RAW.items()}
FALLBACK_GLYPH: tuple[int, ...] = _to_rows(_FALLBACK)


def glyph_rows(ch: str) -> tuple[int, ...]:
    return FONT.get(ch.upper(), FALLBACK_GLYPH)
